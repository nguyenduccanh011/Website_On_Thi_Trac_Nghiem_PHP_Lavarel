@push('scripts')
<script>
    document.addEventListener('DOMContentLoaded', function() {
        // Xử lý sửa danh mục
        const editCategoryModal = document.getElementById('editCategoryModal');
        editCategoryModal.addEventListener('show.bs.modal', function(event) {
            const button = event.relatedTarget;
            const id = button.getAttribute('data-id');
            const name = button.getAttribute('data-name');
            const description = button.getAttribute('data-description');
            
            const form = this.querySelector('#editCategoryForm');
            form.action = `/admin/categories/${id}`;
            
            this.querySelector('#edit_name').value = name;
            this.querySelector('#edit_description').value = description;
        });

        // Xử lý xóa danh mục
        const deleteCategoryModal = document.getElementById('deleteCategoryModal');
        deleteCategoryModal.addEventListener('show.bs.modal', function(event) {
            const button = event.relatedTarget;
            const id = button.getAttribute('data-id');
            const name = button.getAttribute('data-name');
            
            this.querySelector('#delete_category_name').textContent = name;
            this.querySelector('#deleteCategoryForm').action = `/admin/categories/${id}`;
        });
    });
</script>
